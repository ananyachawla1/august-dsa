class Solution {
  public:
    int findKRotation(vector<int> &arr) {
        // Code Here
        int start=0,n=arr.size();
        int end=n-1;
        int mid;
        while(start<=end)
        {
            mid=(start+end)/2;
            if(arr[mid]<arr[0])
            end=mid-1;
            else start=mid+1;
        }
        return start%n;
    }
};


